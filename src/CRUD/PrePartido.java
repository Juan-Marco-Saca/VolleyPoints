    private int crearPartido(){
        int filaSeleccionada = jtEquipos.getSelectedRow();

        int cod_equipo = Integer.parseInt(jtEquipos.getValueAt(filaSeleccionada, 0).toString());
        int year = Integer.parseInt(txtyear.getText());
        int mes = cmbMes.getSelectedIndex();
        String equipo_1 = jtEquipos.getValueAt(filaSeleccionada, 1).toString();
        String equipo_2 = txtEquipoRival.getText();
        String torneo = txtTorneo.getText();
            
        try{
            String SQL = "insert into partido (year, mes, equipo_1, equipo_2, torneo) value (?, ?, ?, ?, ?)";
            
            PreparedStatement pst = con.prepareStatement(SQL);
            pst.setInt(1, year);
            pst.setInt(2,mes);
            pst.setString(3, equipo_1);
            pst.setString(4, equipo_2);
            pst.setString(5, torneo);
            pst.execute();
            

        }catch(Exception e){
            JOptionPane.showMessageDialog(null, "ERROR DE REGISTRO" + e.getMessage());
        } 
        
        return cod_equipo;
    }
        
