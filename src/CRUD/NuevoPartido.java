    private void NuevoSet(){
        if (puntosA >= 25 || puntosB >= 25) {
           if (puntosA >= puntosB + 2) {
                setsA ++;
                jlSetsGanadosA.setText("Sets Ganados: " + Integer.toString(setsA));
                puntosA = 0;
                puntosB = 0;
                jlPuntosB.setText("Puntos: " + Integer.toString(puntosB));
                jlPuntosA.setText("Puntos: " + Integer.toString(puntosA));
                set ++;
                jlSet.setText("Set: " + Integer.toString(set));
                CrearSet();
                NuevoSet.show(true);
                NuevoSet.setSize(997, 883);
                NuevoSet.setLocationRelativeTo(null);
            
            } else if (puntosB >= puntosA + 2) {
                setsB ++;
                jlSetsGanadosB.setText("Sets Ganados: " + Integer.toString(setsB));
                puntosA = 0;
                puntosB = 0;
                jlPuntosB.setText("Puntos: " + Integer.toString(puntosB));
                jlPuntosA.setText("Puntos: " + Integer.toString(puntosA));
                set ++;
                jlSet.setText("Set: " + Integer.toString(set));
                CrearSet();
                NuevoSet.setVisible(true);
                NuevoSet.setSize(997, 883);
                NuevoSet.setLocationRelativeTo(null);
            }
            Ganados();
            if (setsA != 3 || setsB != 3) {
                NuevoSetSetUP();
            } 
        }
        
    }
    
