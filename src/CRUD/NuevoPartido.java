    protected void MostrarJugadores(int [] cod_jugadores, javax.swing.JTable tabla){
   
      String[] titulos = {"Codigo", "Nombre", "Apellido", "Numero", "Posicion", "Codigo Equipo"};
      DefaultTableModel modelo = new DefaultTableModel(null, titulos);
      
      String[] registros = new String[6];
      
      String SQL = "Select * from jugadores";
      
      try{
          
          Statement st = con.createStatement();
          ResultSet rs = st.executeQuery(SQL);
          
          while(rs.next()){
              for (int i = 0; i < cod_jugadores.length; i++) {
                   if(rs.getInt("cod_jugador") == cod_jugadores[i]){
                    registros[0] = rs.getString("cod_jugador");
                    registros[1] = rs.getString("Nombre");
                    registros[2] = rs.getString("Apellidos");
                    registros[3] = rs.getString("Numero_Jugador");
                    registros[4] = rs.getString("Posicion");
                    if(registros[4].equals("1")){
                        registros[4] = "Pasador";
                    } else if(registros[4].equals("2")){
                        registros[4] = "Lateral";
                    }else if(registros[4].equals("3")){
                        registros[4] = "Quinto";
                    } else if(registros[4].equals("4")){
                        registros[4] = "Centro";
                    }else{
                        registros[4] = "Libero";
                    }
                    registros[5] = rs.getString("cod_equipo_de_jugador");  

                    modelo.addRow(registros);
                  }else{

                  }
              }
             
          }
          
          tabla.setModel(modelo);
          
      }catch(Exception e){
          JOptionPane.showMessageDialog(null, "ERROR AL MOSTRAR JUGADORES" + e.getMessage());
      }   
   }
    
    protected void Cambio(){
        int filaSeleccionadaTitular = jtTitulares.getSelectedRow();
        int filaSeleccionadaCambio = jtCambios.getSelectedRow();
        
        String titular [] = new String[6];
        String cambio [] = new String[6];
        
        
        
        if (filaSeleccionadaTitular >= 0 && filaSeleccionadaCambio >= 0) {            
            for (int i = 0; i < 6; i++) {
                titular[i] = jtTitulares.getValueAt(filaSeleccionadaTitular, i).toString();
                cambio[i] = jtCambios.getValueAt(filaSeleccionadaCambio, i).toString();
            }
            
            int codTitular = Integer.parseInt(titular[0]);
            int codCambio = Integer.parseInt(cambio[0]);
            
            for (int i = 0; i < cod_titulares.length; i++) {
                
                if (cod_titulares[i] == codTitular) {
                    cod_titulares[i] = codCambio;
                }
                
            }
            for (int i = 0; i < cod_cambios.length; i++) {
                if (cod_cambios[i] == codCambio) {
                    cod_cambios[i] = codTitular;
                }
            }
            
            for (int i = 0; i < 6; i++) {
               jtTitulares.setValueAt(cambio[i], filaSeleccionadaTitular, i);
               jtCambios.setValueAt(titular[i], filaSeleccionadaCambio, i);
            }
        }
    }

