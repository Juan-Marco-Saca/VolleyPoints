    private DefaultTableModel mostrarPartidos(javax.swing.JTable tabla, DefaultTableModel modelo, String [] registros){




        String SQL = "Select * from partido";

        try{

            Statement st = con.createStatement();
            ResultSet rs = st.executeQuery(SQL);

            while(rs.next()){
                registros[0] = rs.getString("cod_partido");
                registros[1] = rs.getString("year");
                registros[2] = rs.getString("mes");
                registros[3] = rs.getString("equipo_1");
                registros[4] = rs.getString("equipo_2");
                registros[5] = rs.getString("torneo");

                modelo.addRow(registros);
            }

            tabla.setModel(modelo);

        }catch(Exception e){
            JOptionPane.showMessageDialog(null, "ERROR AL MOSTRAR PARTIDOS" + e.getMessage(), "ERROR", JOptionPane.ERROR_MESSAGE);
        }

        return modelo;
    }


